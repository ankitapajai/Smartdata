package com.smartdata.Smartdata.day_one_date12march;

public class Print_Even_Number {
    public static void main(String[] args) {

        // Print even numbers between 1 and 20 using a for loop.
        for(int i = 0; i <= 20; i++){
            if(i % 2 == 0){
                System.out.println(i);
            }
        }
    }
}
